package DomainSource_Package;

public class Programari {
    private int idProgramari;
    private int idPacient;
    private String data;
    private String ora;
    private String specializare;
    private String medic;
    private String servicii;

    public int getIdProgramari() {
        return idProgramari;
    }

    public void setIdProgramari(int idProgramari) {
        this.idProgramari = idProgramari;
    }

    public int getIdPacient() {
        return idPacient;
    }

    public void setIdPacient(int idPacient) {
        this.idPacient = idPacient;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getOra() {
        return ora;
    }

    public void setOra(String ora) {
        this.ora = ora;
    }

    public String getSpecializare() {
        return specializare;
    }

    public void setSpecializare(String specializare) {
        this.specializare = specializare;
    }

    public String getMedic() {
        return medic;
    }

    public void setMedic(String medic) {
        this.medic = medic;
    }

    public String getServicii() {
        return servicii;
    }

    public void setServicii(String servicii) {
        this.servicii = servicii;
    }
}
